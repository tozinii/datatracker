    public function recorridoMapa($code){


          $car = Car::where('code',strtolower($code))->first();

          $coordenadas = [];

          foreach ($car->sensors as $data){
            if ($data->id == 3 ) {
                array_push($coordenadas, $data->pivot->data);
            }
          }

          return view('users/cars',['coordenadas' => $coordenadas]);


    }
